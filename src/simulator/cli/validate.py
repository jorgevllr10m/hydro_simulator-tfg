from __future__ import annotations

import argparse
import csv
from pathlib import Path

import numpy as np


def build_parser() -> argparse.ArgumentParser:
    """Build the CLI parser for validation."""
    parser = argparse.ArgumentParser(
        prog="hydro-sim-validate",
        description="Valida una ejecución del simulador usando los CSV generados.",
    )
    parser.add_argument(
        "--run-dir",
        type=Path,
        required=True,
        help="Ruta al directorio de salida de una ejecución, por ejemplo outputs/runs/base_run",
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=None,
        help="Directorio opcional para las salidas de validación. Por defecto: <run-dir>/validation",
    )
    return parser


def _parse_csv_float(value: str) -> float:
    """Parse one float from the project CSV format.

    Supported inputs
    ----------------
    - comma decimal separator, e.g. '1,250000'
    - standard decimal separator, e.g. '1.250000'
    - 'nan' / empty values
    """
    text = value.strip()

    if text == "":
        return float("nan")

    lowered = text.lower()
    if lowered in {"nan", "none"}:
        return float("nan")

    return float(text.replace(",", "."))


def _parse_csv_int(value: str) -> int:
    """Parse one integer from CSV."""
    text = value.strip()

    if text == "":
        raise ValueError("No se puede interpretar una cadena vacía como entero")

    return int(text)


def _read_semicolon_csv(path: Path) -> list[dict[str, str]]:
    """Read one project CSV file using ';' as delimiter."""
    if not path.exists():
        raise FileNotFoundError(f"No se encontró el archivo CSV requerido: {path}")

    with path.open("r", newline="", encoding="utf-8") as file:
        reader = csv.DictReader(file, delimiter=";")
        return list(reader)


def _write_csv(
    *,
    rows: list[dict[str, object]],
    output_path: Path,
) -> None:
    """Write one CSV using the same spreadsheet-friendly formatting as run.py."""
    output_path.parent.mkdir(parents=True, exist_ok=True)

    if not rows:
        return

    fieldnames = list(rows[0].keys())

    def _format_csv_value(value: object) -> object:
        if isinstance(value, float):
            if np.isnan(value):
                return "nan"
            return f"{value:.6f}".replace(".", ",")
        return value

    formatted_rows = [{key: _format_csv_value(value) for key, value in row.items()} for row in rows]

    with output_path.open("w", newline="", encoding="utf-8") as file:
        writer = csv.DictWriter(file, fieldnames=fieldnames, delimiter=";")
        writer.writeheader()
        writer.writerows(formatted_rows)


def _read_observation_rows(path: Path) -> list[dict[str, object]]:
    """Read and type-cast the observation CSV generated by the runner."""
    raw_rows = _read_semicolon_csv(path)

    parsed_rows: list[dict[str, object]] = []
    for row in raw_rows:
        parsed_rows.append(
            {
                "step": _parse_csv_int(row["step"]),
                "timestamp": row["timestamp"].strip(),
                "sensor_index": _parse_csv_int(row["sensor_index"]),
                "sensor_name": row["sensor_name"].strip(),
                "sensor_type": row["sensor_type"].strip(),
                "cell_y": _parse_csv_int(row["cell_y"]),
                "cell_x": _parse_csv_int(row["cell_x"]),
                "truth_value": _parse_csv_float(row["truth_value"]),
                "observed_value": _parse_csv_float(row["observed_value"]),
                "observation_available": _parse_csv_int(row["observation_available"]),
                "quality_flag": _parse_csv_int(row["quality_flag"]),
                "quality_label": row["quality_label"].strip(),
            }
        )

    return parsed_rows


