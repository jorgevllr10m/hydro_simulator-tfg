def _parse_csv_float(value: str) -> float:
    """Parse one float from the project CSV format."""
    text = value.strip()

    if text == "":
        return float("nan")

    lowered = text.lower()
    if lowered in {"nan", "none"}:
        return float("nan")

    return float(text.replace(",", "."))


def _parse_csv_int(value: str) -> int:
    """Parse one integer from CSV."""
    text = value.strip()

    if text == "":
        raise ValueError("No se puede interpretar una cadena vacía como entero")

    return int(text)


def _read_semicolon_csv(path: Path) -> list[dict[str, str]]:
    """Read one project CSV file using ';' as delimiter."""
    if not path.exists():
        raise FileNotFoundError(f"No se encontró el archivo CSV requerido: {path}")

    with path.open("r", newline="", encoding="utf-8") as file:
        reader = csv.DictReader(file, delimiter=";")
        return list(reader)


